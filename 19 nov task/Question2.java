/*Write a program to make addition, Subtraction, Multiplication and Division of
Two Numbers.*/
public class Question2 {
	public static void main(String args[]) 
	{
		int NUM1 = 10;  // taking variables 
		int NUM2 = 5;
		
	int sum = NUM1 + NUM2;  // adding two number
	int sub = NUM1 - NUM2;  // subtracting two number
	int mul = NUM1 * NUM2;  // multiplication of two number
	int div = NUM1 / NUM2;    // division of two number
	
	System.out.println(sum);    // showing result of all operation 
	System.out.println(sub);
	System.out.println(mul);
	System.out.println(div);
		
	}
	

}
