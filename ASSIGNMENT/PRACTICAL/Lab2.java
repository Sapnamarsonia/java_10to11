//Write a program to demonstrate the use of different data types.
package CoreJava;

public class Lab2 
{
	public static void main(String[] args)
	{
		// Integer Datatype 
		int number = 10;
		System.out.println("Integer : " +number);
		
		// Character Datatype
		char ch = 'A';
		System.out.println("Character is : "+ch);
		
		//String Datatype 
		String s = "Hello world";
		System.out.println("String is : "+s);
		
		//Float Datatype
		float f = (float) 0.5;
		System.out.println("Float value is : "+f);
		
		//Double Datatype
		double d = 1000000 ; 
		System.out.println("Double value is: "+d);
		
		//Boolean Datatype
		boolean  b = false ;
		System.out.println("java is purely Oop? " +b);
		
		//Byte Datatype
		byte bytenum = 123;
		System.out.println("Byte : " +bytenum);
		//Short Datatype
		short sh = 12345;
		System.out.println("Short number : "+ sh);
		//LOng Datetype
		long lg = 987654321;
		System.out.println("Long number : "+ lg);
	
	}
	

}
