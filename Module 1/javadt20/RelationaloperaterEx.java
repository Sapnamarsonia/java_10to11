package javadt20;

public class RelationaloperaterEx 
{
	public static void main(String[] args)
	{
		int a=10; // declaring variable 
		int b=5;
		
		System.out.println(a>b);  // using operator greater then
		System.out.println(a<b);  //using operator less than
		System.out.println(a>=b);  //using operator greater then or equal to
		System.out.println(a<=b);  //using operator less than  or equal to
		System.out.println(a!=b);  //using operator not equal to
		
	}

}
